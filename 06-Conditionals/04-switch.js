const age = 14;

switch (age) {
	case 0:
		console.log("0 is far smaller than age");
		break;
	case 10:
		console.log("10 is less than age");
		break;
	case 14:
		console.log("14 is equal to age");
		break;
	case 16:
		console.log("16 is not that big from age");
		break;
	case 100:
		console.log("100 is far greater than age");
		break;
	default:
		console.log("Non of the above condition is meet");
}
