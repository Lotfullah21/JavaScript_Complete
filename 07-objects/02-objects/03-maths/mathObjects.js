// Math objects

var num = 1.4;
console.log("Ceil", Math.ceil(num));
console.log("round", Math.round(num));
console.log("Floor", Math.floor(num));
console.log("only int", Math.trunc(num));

var num = Math.random();
const newNum = num * 10;
console.log("new random num", num);
console.log("Int of newNum", Math.trunc(newNum));

console.log(Math.PI);
console.log("Logarithm", Math.LN2);
console.log("Euler number", Math.E);
